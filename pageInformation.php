        <h3>QUI SOMMES NOUS?</h3>
        <p><FONT size="1">
            Situé à quelques pas du centre de Roubaix, ville de 98.000 habitants, le lycée polyvalent Jean Moulin accueille actuellement un peu plus de 1.000 élèves et étudiants. <br>
			Il affiche depuis de nombreuses années une identité de lycée formant aux métiers des services, tant en formation initiale (scolaire ou par apprentissage), qu’en formation continue.<br>
			Environ un tiers de ses élèves sont préparés à différents Bac Professionnels tertiaires, un autre tiers à des Bacs généraux et technologiques, et un peu plus de 350 étudiants et apprentis à des BTS également tertiaires.<br>
			Dynamique, il enrichit et développe ses formations en collaborant avec d’autres établissements : le lycée Turgot pour les métiers de l’accueil, du commerce et de la vente, <br>
			le lycée Colbert et l’ESAAT (Ecole Supérieure des Arts Appliqués et du Textile) pour les métiers du web et du e-commerce, le lycée Vertes Feuilles et l’Ecole Nationale de Police pour les métiers de la sécurité.<br>
			Il mène en outre de nombreux partenariats  avec des établissements d’enseignement supérieur, avec l’ESPEME (du groupe EDHEC), ou avec Sciences Po Paris, qui rendent possibles pour ses élèves <br>
			des parcours d’excellence diversifiés.<br>
			Enfin, attentif aux besoins du marché de l’emploi, il innove et crée de nouvelles formations : des formations Bac+1 axées sur les métiers de la sécurité, une  formation Bac+3 dédiée au e-commerce.<br>
			Depuis quelques années, les élèves et tous les membres de la communauté éducative sont rassemblés autour d’un projet, Ensemble pour réussir. Ce slogan marque la volonté de l’établissement de mettre tout en œuvre <br>
			pour que chaque élève qui s’engage activement puisse aller au terme de son propre projet. <br>
			Au-delà de cette volonté, c’est aussi l’expérience, le savoir-faire des équipes pédagogiques et de tous les personnels qui font du lycée Jean Moulin un établissement reconnu pour son haut niveau de performance.<br>
			Chaque élève, chaque étudiant investi dans son projet peut ainsi, quels que soient son niveau et ses difficultés, oser la réussite.
			<br><br>                    </FONT></p>
